#include <stdio.h>
#include <stdarg.h>
#include <stdlib.h>
#include <signal.h>
#include <syscall.h>
#include <sys/ptrace.h>
#include <sys/types.h>
#include <sys/wait.h>
#include <sys/reg.h>
#include <sys/user.h>
#include <unistd.h>
#include <errno.h>



int main(int argc, char** argv)
{
    if (ptrace(PTRACE_TRACEME, 0, 0, 0) < 0) {
        perror("ptrace error");
        return 1;
    }
    return 0;
}


